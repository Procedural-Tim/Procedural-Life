function buildNames(syl1, syl2, syl3) {
  return syl1.reduce((acc, s1) => {
    const base2 = syl2.map((s2) => s1 + s2)

    const base3 = base2.reduce((acc, b2) => {
      return acc.concat(syl3.map((s3) => b2 + s3))
    }, [])

    return acc.concat(base2, base3)
  }, [])
}

